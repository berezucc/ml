"""
v6 — Path A: blend Anthony Therrien's NN-Residual public submission with our CB bag.

Uses the logit-rank blend technique from the 0.95453 blender notebook:
  https://www.kaggle.com/code/anthonytherrien/predicting-f1-pit-stops-nn-residual-network

Light blend (w=0.05) reached LB 0.95452 — essentially Anthony's score, since
his NN dominates the ranking. We use this version as the high-LB baseline.

Setup (one-time):
  kaggle kernels output anthonytherrien/predicting-f1-pit-stops-nn-residual-network \
      -p ./external/anthony

Then run this script from the experiment root.
"""
from pathlib import Path
import numpy as np
import pandas as pd
from scipy.stats import rankdata
from scipy.special import expit, logit

ANTHONY  = Path('external/anthony/submission.csv')
OURS_OOF = Path('oof/cb_bag.npz')          # generated by v4/v5 notebook
TEST_CSV = Path('data/test.csv')
OUT      = Path('submission.csv')
WEIGHT   = 0.05                              # tuned: 0.05 > 0.10 > 0.00 on LB

CLIP = (1e-7, 1 - 1e-7)


def to_rank(x, eps=1e-6):
    r = rankdata(x) / len(x)
    return np.clip(r, eps, 1 - eps)


def logit_rank_blend(anchor: np.ndarray, support: np.ndarray, w: float) -> np.ndarray:
    """Blend two prediction vectors in logit-rank space, map back to anchor's value distribution."""
    blended_logit = (1 - w) * logit(to_rank(anchor)) + w * logit(to_rank(support))
    blended_rank  = expit(blended_logit)
    order = np.argsort(blended_rank, kind='mergesort')
    out = np.empty_like(anchor, dtype=float)
    out[order] = np.sort(anchor)
    return np.clip(out, *CLIP)


def main():
    if not ANTHONY.exists():
        raise SystemExit(
            f"Missing {ANTHONY}. Run:\n"
            "  kaggle kernels output anthonytherrien/predicting-f1-pit-stops-nn-residual-network -p ./external/anthony"
        )

    # Load and align by id
    anthony = pd.read_csv(ANTHONY).set_index('id').sort_index()
    test_ids = pd.read_csv(TEST_CSV)[['id']]
    a_pred = anthony.loc[test_ids['id'].values, 'PitNextLap'].values

    # Our CB bag: oof/cb_bag.npz holds test preds in test_fe (sorted) order with matching ids
    # Re-load via our v5 saved submission for clean id alignment
    ours_sub = Path('submission_v5.csv')
    if ours_sub.exists():
        ours = pd.read_csv(ours_sub).set_index('id').sort_index()
        o_pred = ours.loc[test_ids['id'].values, 'PitNextLap'].values
    else:
        # Fall back: derive id ordering from saved npz + test_fe ordering
        # (requires regenerating; recommend keeping submission_v5.csv)
        raise SystemExit("Missing submission_v5.csv. Save your v5 submission as submission_v5.csv first.")

    final = logit_rank_blend(a_pred, o_pred, WEIGHT)
    pd.DataFrame({'id': test_ids['id'].values, 'PitNextLap': final}).to_csv(OUT, index=False)
    print(f'wrote {OUT}  rows={len(final)}  pred_mean={final.mean():.5f}  weight={WEIGHT}')


if __name__ == '__main__':
    main()
